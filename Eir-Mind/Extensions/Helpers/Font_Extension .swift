import SwiftUI

extension Font {
    
    static func regularFont(size: CGFloat) -> Font {
        return .custom("SFPRODISPLAYREGULAR", size: size)
    }
    
     static func mediumFont(size: CGFloat) -> Font {
        return .custom("", size: size)
    }
    
    
     static func boldFont(size: CGFloat) -> Font {
        return .custom("Poppins-Bold", size: size)
    }
    
}



